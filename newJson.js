function arrangement(j, offset) {
    var len = j.length;
    var ans = [];
    for(var i = 0; i < len; i++)
    {
        var pidx = Math.floor(i/offset);
        for(var j = (-i) + pidx * offset; j + i < (pidx+1) * offset; j++)
        {
            if(j+i != i)
            {
                var curr = [i, j+i];
                ans.push(curr);
            }
            
        }
    }
    return ans;
}

