function selecterPartRandom(offset) {
    var ans = [];

    var oriNow = 0;

    for(var i = 0; i < offset.length; i++)
    {
            var offNow = offset[i] - oriNow;
            for(var j = 1; j < offNow; j++)
            {
                if((Math.random()) > 0.5)
                {
                    var curr = [oriNow, j + oriNow];
                    ans.push(curr);
                } 
                else {
                    var curr_r = [j + oriNow, oriNow];
                    ans.push(curr_r);
                }
            }

            oriNow = offset[i];
    }
    return ans;
}